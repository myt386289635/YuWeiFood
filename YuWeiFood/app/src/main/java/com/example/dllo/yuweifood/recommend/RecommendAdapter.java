import android.content.Context;

import android.util.Log;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.BaseAdapter;
import android.widget.ImageView;
import android.widget.TextView;
import com.example.dllo.yuweifood.R;
import com.squareup.picasso.Picasso;

import java.text.SimpleDateFormat;
import java.util.Date;

public class RecommendAdapter extends BaseAdapter{
    private RecommendBean recommendBean;
    private Context context;
    private SimpleDateFormat simpleDateFormat;

    public RecommendAdapter(Context context) {
        this.context = context;
        simpleDateFormat = new SimpleDateFormat("yyyy.MM.dd");
    }

    public void setRecommendBean(RecommendBean recommendBean) {
        this.recommendBean = recommendBean;
        notifyDataSetChanged();
    }

    @Override
    public int getCount() {
        return recommendBean.getData().getList().get(3).getContent().size();
    }

    @Override
    public Object getItem(int position) {
        return recommendBean.getData().getList().get(3);
    }

    @Override
    public long getItemId(int position) {
        return 0;
    }

    @Override
    public View getView(int position, View convertView, ViewGroup parent) {

        ViewHolder viewHolder = null;

        if (convertView == null){
            convertView = LayoutInflater.from(context).inflate(R.layout.item_recom_listview,parent,false);
            viewHolder = new ViewHolder(convertView);
            convertView.setTag(viewHolder);
        }else{
            viewHolder = (ViewHolder) convertView.getTag();
        }
        Picasso.with(context).load(recommendBean.getData().getList().get(3).getContent().get(position).getCover()).into(viewHolder.iv_cover);
//        String time = simpleDateFormat.format(new Date(recommendBean.getData().getList().get(3).getContent().get(position).getTime()));
//        viewHolder.tv_time.setText(time);
        viewHolder.tv_title.setText(recommendBean.getData().getList().get(3).getContent().get(position).getName());
        viewHolder.tv_uname.setText(recommendBean.getData().getList().get(3).getContent().get(position).getAuthor().getUname());

        return convertView;

    }

    class ViewHolder{
        private TextView tv_time,tv_title,tv_uname;
        private ImageView iv_cover,iv_header;

        public ViewHolder(View view){
            tv_time = (TextView) view.findViewById(R.id.item_text_time);
            tv_title = (TextView) view.findViewById(R.id.item_text_title);
            tv_uname = (TextView) view.findViewById(R.id.item_text_uname);
            iv_cover = (ImageView) view.findViewById(R.id.item_image_cover);
            iv_header = (ImageView) view.findViewById(R.id.item_image_header);
        }
    }
}
