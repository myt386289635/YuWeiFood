import android.support.v7.widget.LinearLayoutManager;
import android.support.v7.widget.RecyclerView;
import com.example.dllo.yuweifood.OKHttp.NetTool;
import com.example.dllo.yuweifood.OKHttp.onHttpCallBack;
import com.example.dllo.yuweifood.R;
import com.example.dllo.yuweifood.base.BaseActivity;
import com.example.dllo.yuweifood.recommend.RecommendBean;


public class ViewPagerHeadActivity extends BaseActivity {
    private RecyclerView recyclerView;
    private RecommendBean recommendBean;
    private int position;
    private String string;
    private ViewHeadAdapter viewHeadAdapter;


    @Override
    protected int setLayout() {
        return R.layout.item_recom_viewpager_head;
    }

    @Override
    protected void initView() {
        recyclerView = (RecyclerView) findViewById(R.id.recycler_view_item);
        viewHeadAdapter = new ViewHeadAdapter(this);
        LinearLayoutManager linearLayoutManager = new LinearLayoutManager(this);
        recyclerView.setLayoutManager(linearLayoutManager);
    }

    @Override
    protected void initDate() {


               NetTool.getInstance().startRequest(getIntent().getStringExtra("position"), ViewPagerHeadBean.class, new onHttpCallBack<ViewPagerHeadBean>() {

                   @Override
                   public void onSuccess(ViewPagerHeadBean response) {
                       viewHeadAdapter.setViewPagerHeadBean(response);
                       recyclerView.setAdapter(viewHeadAdapter);

                   }

                   @Override
                   public void onError(Throwable e) {

                   }
               });


       }


    }


